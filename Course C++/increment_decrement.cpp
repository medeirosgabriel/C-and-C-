#include <iostream>
using namespace std;

int main() {

    int n1 = 0;

    n1 = n1 + 1;
    cout << n1 << endl;

    n1 += 1;
    cout << n1 << endl;

    n1 ++;
    cout << n1 << endl;

    n1 = n1 - 1;
    cout << n1 << endl;

    n1 -= 1;
    cout << n1 << endl;
    
    n1 --;
    cout << n1 << endl;

    cout << n1++ << endl;

    cout << n1 << endl;

    cout << ++n1 << endl;
}